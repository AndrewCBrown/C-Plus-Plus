#include<iostream>
using namespace std;

int main()
{
	//Using one statement with one stream insertion operator.
	cout<<"1 2 3 4\n";
	
	//Using one statement with four stream insertion operators.
	cout<<"1 "<<"2 "<<"3 "<<"4\n";
	
	//Using four statements.
	cout<<"1 ";
	cout<<"2 ";
	cout<<"3 ";
	cout<<"4";
}
